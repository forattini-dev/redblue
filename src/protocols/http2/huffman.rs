/// Decode Huffman-encoded data
pub fn huffman_decode(data: &[u8]) -> Result<Vec<u8>, String> {
    let mut result = Vec::new();
    let mut bits: u64 = 0;
    let mut bits_count: u8 = 0;

    for &byte in data {
        // Add byte to bit buffer (left-aligned)
        bits = (bits << 8) | (byte as u64);
        bits_count += 8;

        // Try to decode symbols while we have enough bits
        while bits_count >= 5 {
            // Try to match a symbol from the Huffman table
            let mut found = false;

            // Check from shortest to longest code
            for length in 5..=30 {
                if bits_count < length {
                    break;
                }

                // Extract the top `length` bits
                let code = (bits >> (bits_count - length)) as u32;
                let mask = (1u32 << length) - 1;
                let code = code & mask;

                // Search for matching code in table
                for (sym, &(table_code, table_len)) in HUFFMAN_TABLE.iter().enumerate() {
                    if table_len == length && table_code == code {
                        result.push(sym as u8);
                        bits_count -= length;
                        found = true;
                        break;
                    }
                }

                if found {
                    break;
                }
            }

            if !found {
                // If we can't match any symbol, we might be at padding or error
                break;
            }
        }
    }

    // Check that remaining bits are all 1s (valid padding)
    if bits_count > 0 {
        let remaining_bits = bits & ((1u64 << bits_count) - 1);
        let expected_padding = (1u64 << bits_count) - 1; // All 1s

        if remaining_bits != expected_padding {
            return Err("Invalid Huffman padding".to_string());
        }
    }

    Ok(result)
}

