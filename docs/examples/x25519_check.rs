use redblue::crypto::x25519::{x25519, x25519_public_key};

fn hex(bytes: &[u8]) -> String {
    bytes.iter().map(|b| format!("{:02x}", b)).collect()
}

fn main() {
    let alice_private = [
        0x77, 0x07, 0x6d, 0x0a, 0x73, 0x18, 0xa5, 0x7d, 0x3c, 0x16, 0xc1, 0x72, 0x51, 0xb2, 0x66,
        0x45, 0xdf, 0x4c, 0x2f, 0x87, 0xeb, 0xc0, 0x99, 0x2a, 0xb1, 0x77, 0xfb, 0xa5, 0x1d, 0xb9,
        0x2c, 0x2a,
    ];
    let bob_private = [
        0x5d, 0xab, 0x08, 0x7e, 0x62, 0x4a, 0x8a, 0x4b, 0x79, 0xe1, 0x7f, 0x8b, 0x83, 0x80, 0x0e,
        0xe6, 0x6f, 0x3b, 0xb1, 0x29, 0x26, 0x18, 0xb6, 0xfd, 0x1c, 0x2f, 0x8b, 0x27, 0xff, 0x88,
        0xe0, 0xeb,
    ];

    let alice_public = x25519_public_key(&alice_private);
    let bob_public = x25519_public_key(&bob_private);

    let alice_shared = x25519(&alice_private, &bob_public);
    let bob_shared = x25519(&bob_private, &alice_public);

    println!("alice_public: {}", hex(&alice_public));
    println!("bob_public:   {}", hex(&bob_public));
    println!("alice_shared: {}", hex(&alice_shared));
    println!("bob_shared:   {}", hex(&bob_shared));

    let expected = [
        0x4a, 0x5d, 0x9d, 0x5b, 0xa4, 0xce, 0x2d, 0xe1, 0x72, 0x8e, 0x3b, 0xf4, 0x80, 0x35,
        0x0f, 0x25, 0xe0, 0x7e, 0x21, 0xc9, 0x47, 0xd1, 0x9e, 0x33, 0x76, 0xf0, 0x9b, 0x3c,
        0x1e, 0x16, 0x17, 0x42,
    ];
    println!("expected:     {}", hex(&expected));

    assert_eq!(alice_shared, bob_shared);
    assert_eq!(alice_shared, expected);

    println!("\nâœ… X25519 vector match");
}
